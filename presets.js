(function() {
    var presetElement = document.querySelector('#presetSelection');

    var selectedHash = location.hash.replace('#', '');

    var presetKeys = Object.keys(presets);
    for(var x = 0; x < presetKeys.length; x++) {
        var option = document.createElement('option');
        option.text = presetKeys[x];

        if(selectedHash === option.text) {
            option.selected = 'selected';
        }

        presetElement.appendChild(option);
    }

    presetElement.addEventListener('change', function(e) {
        location.hash = '#' + presetElement.value;
    });
})();
